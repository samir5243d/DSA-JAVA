package Operators;

public class LogicalOperators {
    public static void main(String[] args) {
        System.out.println((3 > 2) && (5 > 0)); // and and operator- if both true then true otherwise false
        System.out.println((3 > 4) || (2 > 1));  // or operator- if both false then false otherwise true
        System.out.println(!(2 > 5)); // logical not operator reverse ans if true it converts false


    }
}
